"""
R1 refresh: rebuild the matched leaderboard + family contrasts + equivalence/power with the
RE-TUNED RF/XGB (selected on LOPO-RMSE), and print OLD (Pearson-r-tuned RF) vs NEW side by side.

Reuses the EXACT methodology of:
  experiments/pmea_p0/p0_master_analysis.py      (align + matched master build)
  experiments/pmea_p1/p1_significance.py         (Friedman, Holm-Wilcoxon, family-mean + Holm-over-3)
  experiments/pmea_p1/p1_equivalence_power.py    (MDE, achieved power, TOST)

Run from the repository root AFTER optuna_rf.py and optuna_xgb.py have produced best_params.json:
    python experiments/pmea_r1_matched_tuning/refresh_retuned.py
Outputs -> results/pmea_r1_matched_tuning/
"""
import json, itertools
from pathlib import Path
import numpy as np, pandas as pd
from scipy.signal import butter, filtfilt
from scipy import stats
from scipy.stats import friedmanchisquare, wilcoxon
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_squared_error
import xgboost as xgb

RES = Path("results"); OUT = RES / "pmea_r1_matched_tuning"; OUT.mkdir(parents=True, exist_ok=True)
N = 16; ALPHA = 0.05; POWER = 0.80; MARGINS = [0.25, 0.50]

LINEAR = ["LINEAR", "RIDGE", "LASSO", "ELASTICNET"]; TREE = ["RF", "XGB"]
TEMP = ["GRU", "LSTM", "TCN", "TFT", "PATCHTST"]; ALL = LINEAR + TREE + TEMP
FAMILIES = {"Linear": LINEAR, "Tree": TREE, "Temporal": TEMP}
FAM_OF = {m: f for f, ms in FAMILIES.items() for m in ms}

# ---------------------------------------------------------------------------
# tree preprocessing (identical to the shared tree-preprocessing routine)
# ---------------------------------------------------------------------------
def butter_lowpass_filter(data, cutoff=0.01, fs=1.0, order=2):
    b, a = butter(order, cutoff / (0.5 * fs), btype="low")
    return filtfilt(b, a, data, axis=0)

def load_and_prepare(csv_path):
    df = pd.read_csv(csv_path); pidx = df["ID"].values
    y = df["VO2"].values / df["Weight"].values
    Nn = len(df); X = np.zeros((Nn, 6))
    X[:, 0] = df["BF"]; X[:, 1] = df["VE"]; X[:, 2] = df["ACC"]; X[:, 3] = df["HR"]; X[:, 4] = df["CAD"]
    for pid in np.unique(pidx):
        idx = (pidx == pid); hr = X[idx, 3]; d = np.zeros(len(hr)); d[1:] = hr[1:] - hr[:-1]; X[idx, 5] = d
    return pidx, butter_lowpass_filter(X), y, np.unique(pidx)

def gen_tree_preds(model_name, params):
    pidx, X, y, parts = load_and_prepare("data/TB-File-01.csv")
    if model_name == "RF":
        mk = lambda: RandomForestRegressor(n_jobs=-1, random_state=42, **params)
    else:
        mk = lambda: xgb.XGBRegressor(random_state=42, n_jobs=-1, tree_method="hist", **params)
    rows = []
    for test_pid in parts:
        tr, te = pidx != test_pid, pidx == test_pid
        m = mk(); m.fit(X[tr], y[tr]); yp = m.predict(X[te])
        for yt, yh in zip(y[te], yp):
            rows.append((test_pid, yt, yh))
    out = pd.DataFrame(rows, columns=["participant", "y_true", "y_pred"])
    d = OUT / model_name; d.mkdir(parents=True, exist_ok=True)
    out.to_csv(d / "y_yhat.csv", index=False)
    return out

# ---------------------------------------------------------------------------
# matched-master build (identical align() to p0_master_analysis.py)
# ---------------------------------------------------------------------------
raw = pd.read_csv("data/TB-File-01.csv").sort_values("ID", kind="stable").reset_index(drop=True)
raw["t"] = raw.groupby("ID").cumcount()
raw["vo2kg"] = raw["VO2"] / raw["Weight"]
rc = raw.groupby("ID").size().to_dict()
truth = raw.set_index(["ID", "t"])["vo2kg"]

def align(df):
    out = []
    for p, g in df.groupby("participant", sort=True):
        base = rc[p] - len(g); best_s, best_e = 0, np.inf
        for s in range(-3, 4):
            t = np.arange(base + s, base + s + len(g)); ok = (t >= 0) & (t < rc[p])
            tv = truth.loc[p].reindex(t[ok]).values
            e = np.nanmean(np.abs(g["y_true"].values[ok] - tv))
            if e < best_e: best_e, best_s = e, s
        g = g.copy(); g["t"] = np.arange(base + best_s, base + best_s + len(g)); out.append(g)
    res = pd.concat(out, ignore_index=True); return res[res.t >= 0]

OLD_FILES = {  # per-model prediction sources; RF/XGB are regenerated by gen_tree_preds below (their path value is unused)
    "LINEAR": "linear_fixed/LINEAR/y_yhat.csv", "RIDGE": "linear_fixed/RIDGE/y_yhat.csv",
    "LASSO": "linear_fixed/LASSO/y_yhat.csv", "ELASTICNET": "linear_fixed/ELASTICNET/y_yhat.csv",
    "RF": "pmea_r1_matched_tuning/RF/y_yhat.csv", "XGB": "pmea_r1_matched_tuning/XGB/y_yhat.csv",
    "GRU": "optuna_temporal/GRU/y_yhat.csv", "LSTM": "optuna_temporal/LSTM/y_yhat.csv",
    "TCN": "optuna_temporal/TCN/y_yhat.csv", "TFT": "optuna_temporal/TFT/y_yhat.csv",
    "PATCHTST": "optuna_temporal/PATCHTST/y_yhat.csv",
}

def build_master(pred_frames):
    M = raw[["ID", "t", "vo2kg"]].rename(columns={"ID": "participant", "vo2kg": "y_true"}).copy()
    for m in ALL:
        a = align(pred_frames[m])
        M = M.merge(a[["participant", "t", "y_pred"]].rename(columns={"y_pred": m}),
                    on=["participant", "t"], how="inner")
    return M

def per_participant_rmse_matrix(M):
    def per_block(g):
        return pd.Series({m: np.sqrt(np.mean((g[m].values - g["y_true"].values) ** 2)) for m in ALL})
    R = M.groupby("participant", sort=True).apply(per_block, include_groups=False)[ALL]
    return R

# ---------------------------------------------------------------------------
# stats helpers (from p1 scripts)
# ---------------------------------------------------------------------------
def holm(pvals):
    pvals = np.asarray(pvals, float); m = len(pvals); order = np.argsort(pvals)
    corr = np.empty(m); run = 0.0
    for rank, idx in enumerate(order):
        adj = min((m - rank) * pvals[idx], 1.0); run = max(run, adj); corr[idx] = run
    return corr

def mde_paired(sd_diff, n=N, alpha=ALPHA, power=POWER):
    df_t = n - 1; t_crit = stats.t.ppf(1 - alpha / 2, df_t); se = sd_diff / np.sqrt(n)
    f = lambda ncp: stats.nct.sf(t_crit, df_t, ncp) + stats.nct.cdf(-t_crit, df_t, ncp)
    lo, hi = 0.0, 50.0
    while f(hi) < power and hi < 1e6: hi *= 2
    for _ in range(200):
        mid = 0.5 * (lo + hi)
        if f(mid) < power: lo = mid
        else: hi = mid
    ncp = 0.5 * (lo + hi); return ncp * se

def achieved_power(mean_diff, sd_diff, n=N, alpha=ALPHA):
    df_t = n - 1; t_crit = stats.t.ppf(1 - alpha / 2, df_t); se = sd_diff / np.sqrt(n)
    ncp = mean_diff / se
    return stats.nct.sf(t_crit, df_t, ncp) + stats.nct.cdf(-t_crit, df_t, ncp)

def tost(d, margin, n=N):
    df_t = n - 1; mean = np.mean(d); sd = np.std(d, ddof=1); se = sd / np.sqrt(n)
    p_lower = stats.t.sf((mean + margin) / se, df_t)
    p_upper = stats.t.cdf((mean - margin) / se, df_t)
    return max(p_lower, p_upper)

def headline(R, tag):
    """Compute all headline numbers from a 16x11 per-participant RMSE matrix."""
    mean_rmse = R.mean()
    fried = friedmanchisquare(*[R[m].values for m in ALL])
    # pairwise Holm-Wilcoxon
    praw = []
    for a, b in itertools.combinations(ALL, 2):
        try: p = wilcoxon(R[a].values, R[b].values, alternative="two-sided").pvalue
        except ValueError: p = 1.0
        praw.append(p)
    pholm = holm(praw); n_surv = int((pholm < 0.05).sum()); smallest_holm = float(np.min(pholm))
    # family-mean + Holm over 3
    fam_mean = pd.DataFrame({f: R[ms].mean(axis=1) for f, ms in FAMILIES.items()})
    fam_best = pd.DataFrame({f: R[ms].min(axis=1) for f, ms in FAMILIES.items()})
    fam_pairs = [("Temporal", "Linear"), ("Temporal", "Tree"), ("Tree", "Linear")]
    fp_raw = [wilcoxon(fam_mean[a].values, fam_mean[b].values, alternative="two-sided").pvalue
              for a, b in fam_pairs]
    fp_holm = holm(fp_raw)
    fam = {f"{a}_vs_{b}": {"p_raw_meanfam": float(pr), "p_holm_meanfam": float(ph),
                           "p_bestinfam": float(wilcoxon(fam_best[a].values, fam_best[b].values,
                                                         alternative="two-sided").pvalue)}
           for (a, b), pr, ph in zip(fam_pairs, fp_raw, fp_holm)}
    # equivalence/power
    contrasts = {"TCN_vs_RF": (R["TCN"], R["RF"]), "TCN_vs_RIDGE": (R["TCN"], R["RIDGE"]),
                 "Temporal_vs_Tree": (fam_mean["Temporal"], fam_mean["Tree"])}
    eqp = {}
    for name, (a, b) in contrasts.items():
        d = (a.values - b.values); gap = abs(float(a.mean() - b.mean()))
        sd = float(np.std(d, ddof=1)); mde = float(mde_paired(sd))
        eqp[name] = {"gap": gap, "sd_diff": sd, "mde": mde, "gap_below_mde": gap < mde,
                     "achieved_power": float(achieved_power(abs(np.mean(d)), sd)),
                     "tost_0.25": float(tost(d, 0.25)), "tost_0.50": float(tost(d, 0.50))}
    return {"tag": tag, "leaderboard": {m: float(mean_rmse[m]) for m in ALL},
            "friedman_chi2": float(fried.statistic), "friedman_p": float(fried.pvalue),
            "pairwise_n_survive_holm": n_surv, "pairwise_smallest_holm": smallest_holm,
            "family": fam, "equivalence_power": eqp}

# ---------------------------------------------------------------------------
# RUN
# ---------------------------------------------------------------------------
print("=" * 80)
rf_bp = json.load(open(OUT / "RF" / "best_params.json"))["params"]
xgb_bp = json.load(open(OUT / "XGB" / "best_params.json"))["params"]
print(f"[retune] RF best params:  {rf_bp}")
print(f"[retune] XGB best params: {xgb_bp}")

print("[gen] regenerating RF/XGB predictions on LOPO ...")
new_pred = {}
for m, f in OLD_FILES.items():
    if m == "RF":   new_pred[m] = gen_tree_preds("RF", rf_bp)
    elif m == "XGB": new_pred[m] = gen_tree_preds("XGB", xgb_bp)
    else:            new_pred[m] = pd.read_csv(RES / f)

M_new = build_master(new_pred)
M_new.to_parquet(OUT / "master_aligned_retuned.parquet")
R_new = per_participant_rmse_matrix(M_new)
R_new.to_csv(OUT / "per_participant_rmse_retuned.csv")
print(f"[matched] NEW common set: {len(M_new)} samples, {M_new.participant.nunique()} participants")

# OLD: recompute headline from the existing master (Pearson-r-tuned RF/XGB) for apples-to-apples
M_old = pd.read_parquet(RES / "pmea_p0" / "master_aligned.parquet")
def per_block_old(g): return pd.Series({m: np.sqrt(np.mean((g[m].values - g["y_true"].values) ** 2)) for m in ALL})
R_old = M_old.groupby("participant", sort=True).apply(per_block_old, include_groups=False)[ALL]

H_old = headline(R_old, "OLD (RF tuned on Pearson r)")
H_new = headline(R_new, "NEW (RF/XGB tuned on LOPO-RMSE)")
json.dump({"old": H_old, "new": H_new}, open(OUT / "headline_old_vs_new.json", "w"), indent=2)

def show(H):
    print(f"\n----- {H['tag']} -----")
    lb = sorted(H["leaderboard"].items(), key=lambda kv: kv[1])
    print("  leaderboard (mean per-participant RMSE):")
    for m, v in lb: print(f"    {m:<11}{v:.3f}  [{FAM_OF[m]}]")
    print(f"  Friedman chi2={H['friedman_chi2']:.2f}  p={H['friedman_p']:.2e}")
    print(f"  pairwise Holm: {H['pairwise_n_survive_holm']}/55 survive; smallest Holm p={H['pairwise_smallest_holm']:.4f}")
    print("  family contrasts (family-mean Wilcoxon, Holm over 3):")
    for k, v in H["family"].items():
        print(f"    {k:<20} p_raw={v['p_raw_meanfam']:.4f}  p_Holm={v['p_holm_meanfam']:.4f}  (best-in-fam p={v['p_bestinfam']:.4f})")
    print("  equivalence/power:")
    for k, v in H["equivalence_power"].items():
        print(f"    {k:<18} gap={v['gap']:.3f} MDE={v['mde']:.3f} power={v['achieved_power']:.2f} "
              f"TOST0.25={v['tost_0.25']:.3f} TOST0.50={v['tost_0.50']:.3f}")

show(H_old); show(H_new)

print("\n" + "=" * 80)
