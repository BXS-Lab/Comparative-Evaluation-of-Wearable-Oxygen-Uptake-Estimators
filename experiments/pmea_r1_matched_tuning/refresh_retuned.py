for k in ["Temporal_vs_Linear", "Tree_vs_Linear"]:
    o = H_old["family"][k]["p_holm_meanfam"]; n = H_new["family"][k]["p_holm_meanfam"]
    verdict = "SURVIVES" if n < 0.05 else "DOES NOT SURVIVE"
    print(f"  {k}: old p_Holm={o:.4f} -> new p_Holm={n:.4f}  [{verdict}]")
eq_o = H_old["equivalence_power"]["TCN_vs_RF"]; eq_n = H_new["equivalence_power"]["TCN_vs_RF"]
print(f"  TCN-vs-RF gap: {eq_o['gap']:.3f} -> {eq_n['gap']:.3f}; "
      f"TOST0.25 {eq_o['tost_0.25']:.3f} -> {eq_n['tost_0.25']:.3f}")
print(f"\n[saved] {OUT}/headline_old_vs_new.json, master_aligned_retuned.parquet, per_participant_rmse_retuned.csv")
print("=" * 80)
